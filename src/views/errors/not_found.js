import m from 'mithril'
import i18n from 'locale.yaml'

// ============================================================================
// Views
// ============================================================================

const view =
  vnode => {
    let { errors } = i18n[navigator.language]
    return m('div', errors.page_not_found)
  }

// ============================================================================
// Export
// ============================================================================

export default {
  view
}
